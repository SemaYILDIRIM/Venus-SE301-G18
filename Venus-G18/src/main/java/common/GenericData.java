package common;

/**
 *
 * @author packard bell
 */
public class GenericData {
    private int key;
    private String value;

    public GenericData(int key, String value) {
        this.key = key;
        this.value = value;
    }

    public int getKey() {
        return key;
    }

    public String getValue() {
        return value;
    }
}
